package io.speejson.exception;

public class ReflectionException extends RuntimeException {

	private static final long serialVersionUID = -948566685990807428L;

	public ReflectionException() {
		super();
	}

	public ReflectionException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ReflectionException(String message, Throwable cause) {
		super(message, cause);
	}

	public ReflectionException(String message) {
		super(message);
	}

	public ReflectionException(Throwable cause) {
		super(cause);
	}

	
	
}
